int		ft_sqrt(int nb)
{
	int sqr;

	sqr = 0;
	if (nb < 0)
		return (0);
	else
	{
		while ((sqr * sqr < nb) && (sqr < 46340))
			++sqr;
		if (sqr * sqr == nb)
			return (sqr);
	}
	return (0);
}
